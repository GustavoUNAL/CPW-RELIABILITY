export function strategyMix(plans: MaintenanceOptimizationPlan[]) {
  const counts = new Map<MaintenanceStrategy, number>();
  for (const p of plans) {
    counts.set(p.recommendedStrategy, (counts.get(p.recommendedStrategy) ?? 0) + 1);
  }
  const total = plans.length || 1;
  return [...counts.entries()]
    .map(([name, count]) => ({ name, count, pct: Math.round((count / total) * 100) }))
    .sort((a, b) => b.count - a.count);
}

export function topOpportunities(plans: MaintenanceOptimizationPlan[], limit = 4) {
  return [...plans]
    .filter((p) => p.healthBand !== "Excelente" || p.optimizationStatus === "Requiere optimización")
    .sort((a, b) => a.mphi - b.mphi || b.impactIndex - a.impactIndex)
    .slice(0, limit)
    .map((p) => ({
      assetId: p.assetId,
      action: p.recommendations[0]?.recommendation ?? p.expectedBenefit,
      stars: p.impactIndex >= 8 ? 5 : p.impactIndex >= 6 ? 4 : p.impactIndex >= 4 ? 3 : 2,
      mphi: p.mphi,
    }));
}

export function portfolioKpis(plans: MaintenanceOptimizationPlan[]) {
  const optimized = plans.filter(
    (p) => p.optimizationStatus === "Implementado" || p.optimizationStatus === "Validado",
  ).length;
  const avgCostSaving =
    plans.reduce((acc, p) => acc + Math.abs(Math.min(0, p.benefits.costPct)), 0) /
    (plans.length || 1);
  const avgAvailGain =
    plans.reduce((acc, p) => acc + p.benefits.availabilityPct, 0) / (plans.length || 1);
  const avgFailDrop =
    plans.reduce((acc, p) => acc + Math.abs(p.benefits.failuresPct), 0) / (plans.length || 1);
  return {
    assetsAnalyzed: plans.length,
    plansEvaluated: plans.length,
    plansOptimized: optimized,
    estimatedSavingsPct: Number(avgCostSaving.toFixed(1)),
    expectedAvailabilityGain: Number(avgAvailGain.toFixed(2)),
    expectedFailureReduction: Math.round(avgFailDrop),
  };
}
