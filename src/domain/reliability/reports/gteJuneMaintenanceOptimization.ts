import {
  computeMphi,
  evaluationFromMphi,
  healthBandFromMphi,
  type CriticalityLevel,
  type ExpectedBenefits,
  type MaintenanceOptimizationPlan,
  type MaintenanceRecommendation,
  type MaintenanceStrategy,
  type EquipmentType,
  type OptimizationStatus,
} from "./maintenanceOptimizationTypes";

function rec(
  planId: string,
  n: number,
  recommendation: string,
  priority: MaintenanceRecommendation["priority"],
  expectedBenefit: string,
  estimatedSavingsPct: number,
  implementationCost: number,
  responsible: string,
  status: MaintenanceRecommendation["status"],
  autoGenerated: boolean,
  trigger?: string,
): MaintenanceRecommendation {
  return {
    id: `${planId}-R${n}`,
    planId,
    recommendation,
    priority,
    expectedBenefit,
    estimatedSavingsPct,
    implementationCost,
    responsible,
    status,
    createdAt: "2026-06-30",
    autoGenerated,
    trigger,
  };
}

function plan(input: {
  id: string;
  assetId: string;
  assetName: string;
  equipmentType: EquipmentType;
  manufacturer: string;
  criticality: CriticalityLevel;
  currentStrategy: MaintenanceStrategy;
  currentFrequency: string;
  recommendedStrategy: MaintenanceStrategy;
  recommendedFrequency: string;
  optimizationStatus: OptimizationStatus;
  justification: string;
  expectedBenefit: string;
  benefits: ExpectedBenefits;
  operatingHours: number;
  availabilityPct: number;
  mtbfHours: number | null;
  mttrHours: number | null;
  failures: number;
  maintenanceCostUsd: number;
  outageHours: number;
  impactIndex: number;
  linkedRcaIds: string[];
  linkedPlanIds: string[];
  linkedEventHints: string[];
  recommendations: MaintenanceRecommendation[];
}): MaintenanceOptimizationPlan {
  const mphi = computeMphi({
    criticality: input.criticality,
    availabilityPct: input.availabilityPct,
    failures: input.failures,
    mtbfHours: input.mtbfHours,
    mttrHours: input.mttrHours,
    impactIndex: input.impactIndex,
  });
  return {
    ...input,
    mphi,
    healthBand: healthBandFromMphi(mphi),
    evaluation: evaluationFromMphi(mphi),
    createdAt: "2026-06-30",
    updatedAt: "2026-07-21",
  };
}

